public class Solares {
    private int Ancho;
    private int Largo;
    
    public Solares(){
        
    }

    public Solares(int Ancho, int Largo) {
        this.Ancho = Ancho;
        this.Largo = Largo;
    }

    public int getAncho() {
        return Ancho;
    }

    public void setAncho(int Ancho) {
        this.Ancho = Ancho;
    }

    public int getLargo() {
        return Largo;
    }

    public void setLargo(int Largo) {
        this.Largo = Largo;
    }
    
    
}
